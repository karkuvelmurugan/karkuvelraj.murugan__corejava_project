package Misc;

public class Academy {

	String name;
	int id;
	String code;

	public String getName() {
		return name;
	}

	public void setName(String newName) {
		name = newName;
	}

	public int getId() {
		return id;
	}

	public void setId(int newId) {
		id = newId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String newCode) {
		code = newCode;
	}
}